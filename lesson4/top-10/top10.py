import sys
import csv


def mapper():
    reader = csv.reader(sys.stdin, delimiter='\t')
    writer = csv.writer(sys.stdout, delimiter='\t', quotechar='"', quoting=csv.QUOTE_ALL)

    n = 10
    topn = []

    for line in reader:
        if len(line) > 5:
            topn.append(line)
            topn = sorted(topn, key=lambda key: len(key[4]))

            if len(topn) > n:
                topn.pop(0)

    for key in topn:
        writer.writerow(key)

